
function age_calculation(type, name, years){
  return `My ${type}, ${name}, is ${years} (${years*12} months).`}

age_calculation("mother", "Marlo", 47)
// "My mother, Marlo, is 47 (564 months)."

age_calculation("brother", "Matt", 22)
// "My brother, Matt, is 22 (264 months)."

age_calculation("sister", "Kaitlin", 20)
// "My sister, Kaitlin, is 20 (240 months)."
